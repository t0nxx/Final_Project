    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String name = jTextField_name.getText();
        String address = jTextField_address.getText();
        String rank_String = jTextField_rank.getText();
        ////herr to get the inputs as text
        String id_String = jTextField_id.getText();
        String whours_String = jTextField_whours.getText();
        String ohours_String = jTextField_ohours.getText();
        /// now we parse the text of int,double values
        int id = Integer.parseInt(id_String);
        Integer rank = Integer.parseInt(rank_String);
        int whours = Integer.parseInt(whours_String);
        int ohours = Integer.parseInt(ohours_String);
        // vaildate enterd not empty
        String [] checkEmpty = {jTextField_name.getText(),jTextField_address.getText()};
            for (String check1 : checkEmpty) {
                if (check1 == null || check1.equals("")) {
                    throw new NullPointerException() ;
                }
            }
            if (rank == 3 || rank == 1 || rank == 2) {
            } else {
                throw new Exception("rank must be 1 , 2 , 3");
            }

        //// inital new employeee                                    *******************************************
        Employee e = new Employee(id, name, address, rank, whours, ohours);
        //add employee to table
         DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
         model.addRow(new Object[] {e.getId(),e.getName(),e.getAddress(),e.getRank(),e.getHoursWorked(),e.getOvertimeHours(),e.getSalary()});

        } catch (NullPointerException e1) {
            JOptionPane.showMessageDialog(null, "All fields must me Enterd");
        }
        catch(NumberFormatException e2){
            JOptionPane.showMessageDialog(null, "ID , w.hours , o,Hours , rank must be Numbers");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }



    }//GEN-LAST:event_jButton1ActionPerformed

