    private void exportFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportFileActionPerformed
        try {
             File f = new File("export.txt");
             //check if exsistance
             if (!f.exists()) {
                f.createNewFile();
            }
             FileWriter fw = new FileWriter(f.getAbsoluteFile());
             BufferedWriter bw = new BufferedWriter(fw);

             int numOfRows = jTable1.getRowCount();
             int numOfCol = jTable1.getColumnCount();
             //check field is in table
             if (numOfRows < 1) throw new Exception("no table filed to export");
             for (int i = 0; i < numOfRows; i++) {
                 for (int j = 0; j < numOfCol; j++) {
                     bw.write(jTable1.getModel().getValueAt(i,j)+",");
                 }
                 bw.newLine();
            }
            bw.close(); fw.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_exportFileActionPerformed

