    private void avgSal_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_avgSal_btnActionPerformed
        try {
        int numOfRows = jTable1.getRowCount();
        if (numOfRows < 1) throw new Exception("no table filed to calculate");
        double avg = 0 ;
        for (int i = 0; i < numOfRows; i++) {
            avg += Double.parseDouble(jTable1.getValueAt(i, 6).toString()); // 6 is colmn of salary

        }
        avg=avg/numOfRows;
        jTextPane3.setText(String.valueOf(avg));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_avgSal_btnActionPerformed

